void MApp_TranslateCharTableToUnicode(BYTE *ChannelNameBuffer, WORD *NameArray, BYTE BufferMaxLen)
{
	BYTE u8Offset = 0, u81stByte, i, j;
	EN_CHARACTER_CODE_TABLE enTable;
	u81stByte = ChannelNameBuffer[0];
	u8Offset = 1;
	enTable = UNSUPPORT_CHAR_TABLE;
	if (u81stByte >= 0x80 && u81stByte <= 0x9F)
	{
		u81stByte = ChannelNameBuffer[1];
		u8Offset = 2;
	}
	memset(NameArray, 0, BufferMaxLen * 2);
	switch (u81stByte)
	{
	case 0x01: enTable = ISO_8859_05; break;
	case 0x02: enTable = ISO_8859_06; break;
	case 0x03: enTable = ISO_8859_07; break;
	case 0x04: enTable = ISO_8859_08; break;
	case 0x05: enTable = ISO_8859_09; break;
	case 0x06: enTable = ISO_8859_10; break;
	case 0x07: enTable = ISO_8859_11; break;
	case 0x09: enTable = ISO_8859_13; break;
	case 0x0A: enTable = ISO_8859_14; break;
	case 0x0B: enTable = ISO_8859_15; break;
	case 0x10:
	{
				 if (ChannelNameBuffer[1] == 0)
				 {
					 switch (ChannelNameBuffer[2])
					 {
					 case 0x01: enTable = ISO_8859_01; u8Offset = 3; break;
					 case 0x02: enTable = ISO_8859_02; u8Offset = 3; break;
					 case 0x03: enTable = ISO_8859_03; u8Offset = 3; break;
					 case 0x04: enTable = ISO_8859_04; u8Offset = 3; break;
					 case 0x05: enTable = ISO_8859_05; u8Offset = 3; break;
					 case 0x06: enTable = ISO_8859_06; u8Offset = 3; break;
					 case 0x07: enTable = ISO_8859_07; u8Offset = 3; break;
					 case 0x08: enTable = ISO_8859_08; u8Offset = 3; break;
					 case 0x09: enTable = ISO_8859_09; u8Offset = 3; break;
					 case 0x0A: enTable = ISO_8859_10; u8Offset = 3; break;
					 case 0x0B: enTable = ISO_8859_11; u8Offset = 3; break;
					 case 0x0D: enTable = ISO_8859_13; u8Offset = 3; break;
					 case 0x0E: enTable = ISO_8859_14; u8Offset = 3; break;
					 case 0x0F: enTable = ISO_8859_15; u8Offset = 3; break;
					 }
				 }
	}
		break;
	}
	if (enTable == UNSUPPORT_CHAR_TABLE)//no show channel name
	{
		return;
	}
	for (i = u8Offset, j = 0; i<BufferMaxLen; i++)
	{
		if (ChannelNameBuffer[i] == 0)
			break;
		else if (ChannelNameBuffer[i] < 0x80)
		{
			NameArray[j] = (unsigned short)ChannelNameBuffer[i];
			j++;
		}
		else
		{
			if (ISO_CHARACTER_TABLES[enTable][ChannelNameBuffer[i] - 0x80] != 0x00 && ISO_CHARACTER_TABLES[enTable][ChannelNameBuffer[i] - 0x80] != 0x00AD)
			{
				NameArray[j] = ISO_CHARACTER_TABLES[enTable][ChannelNameBuffer[i] - 0x80];
				if (NameArray[j] >= 0x80 && NameArray[j] <= 0x9F)
					NameArray[j] = (0xE000 | NameArray[j]);
				j++;
			}
		}
	}
}
BOOL MApp_TranslateUnicodeToCharTable(BYTE *ChannelNameBuffer, WORD *NameArray, BYTE BufferMaxLen)
{
	BYTE u8Offset = 0, u81stByte, i, j;
	EN_CHARACTER_CODE_TABLE enTable;
	BOOL bRet = TRUE;
	u81stByte = ChannelNameBuffer[0];
	u8Offset = 1;
	enTable = UNSUPPORT_CHAR_TABLE;
	if (u81stByte >= 0x80 && u81stByte <= 0x9F)
	{
		u81stByte = ChannelNameBuffer[1];
		u8Offset = 2;
	}
	switch (u81stByte)
	{
	case 0x01: enTable = ISO_8859_05; break;
	case 0x02: enTable = ISO_8859_06; break;
	case 0x03: enTable = ISO_8859_07; break;
	case 0x04: enTable = ISO_8859_08; break;
	case 0x05: enTable = ISO_8859_09; break;
	case 0x06: enTable = ISO_8859_10; break;
	case 0x07: enTable = ISO_8859_11; break;
	case 0x09: enTable = ISO_8859_13; break;
	case 0x0A: enTable = ISO_8859_14; break;
	case 0x0B: enTable = ISO_8859_15; break;
	case 0x10:
	{
				 if (ChannelNameBuffer[1] == 0)
				 {
					 switch (ChannelNameBuffer[2])
					 {
					 case 0x01: enTable = ISO_8859_01; u8Offset = 3; break;
					 case 0x02: enTable = ISO_8859_02; u8Offset = 3; break;
					 case 0x03: enTable = ISO_8859_03; u8Offset = 3; break;
					 case 0x04: enTable = ISO_8859_04; u8Offset = 3; break;
					 case 0x05: enTable = ISO_8859_05; u8Offset = 3; break;
					 case 0x06: enTable = ISO_8859_06; u8Offset = 3; break;
					 case 0x07: enTable = ISO_8859_07; u8Offset = 3; break;
					 case 0x08: enTable = ISO_8859_08; u8Offset = 3; break;
					 case 0x09: enTable = ISO_8859_09; u8Offset = 3; break;
					 case 0x0A: enTable = ISO_8859_10; u8Offset = 3; break;
					 case 0x0B: enTable = ISO_8859_11; u8Offset = 3; break;
					 case 0x0D: enTable = ISO_8859_13; u8Offset = 3; break;
					 case 0x0E: enTable = ISO_8859_14; u8Offset = 3; break;
					 case 0x0F: enTable = ISO_8859_15; u8Offset = 3; break;
					 }
				 }
	}
		break;
	}
	if (enTable == UNSUPPORT_CHAR_TABLE)//no show channel name
	{
		enTable = ISO_8859_15;
		ChannelNameBuffer[0] = 0x0B;
		u8Offset = 1;
	}
	for (i = 0; i<BufferMaxLen; i++)
	{
		if (NameArray[i] == 0)
			break;
		if (NameArray[i] >= 0xE080 && NameArray[i] <= 0xE09F)
		{
			NameArray[i] &= 0x00FF;
		}
	}
	for (i = 0, j = u8Offset; i<BufferMaxLen; i++)
	{
		BYTE k;
		if (NameArray[i] == 0)
		{
			ChannelNameBuffer[j] = 0;
			j++;
			break;
		}
		if (NameArray[i]<0x80)
		{
			ChannelNameBuffer[j] = NameArray[i];
			j++;
		}
		else
		{
			for (k = 0; k<128; k++)
			{
				if (ISO_CHARACTER_TABLES[enTable][k] == NameArray[i])
					break;
			}
			if (k >= 128)//no found,return FALSE
			{
				bRet = FALSE;
				ChannelNameBuffer[j] = 0x20;//use space char
				j++;
			}
			else
			{
				ChannelNameBuffer[j] = 0x80 + k;
				j++;
			}
		}
	}
	ChannelNameBuffer[j] = 0;
	return bRet;
}